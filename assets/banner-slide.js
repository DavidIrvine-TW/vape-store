document.addEventListener('DOMContentLoaded', function () {
    const slideImages = [
      "{{ '791f1e59e84bba5802cd92984887bf4a.gif' | asset_url }}",
      "{{ 'GIRL-VAPE-GIF.gif' | asset_url }}"
    ];

    let step = 0;

    function slideIt() {
      if (!document.images) return;

      const slideElement = document.getElementById('slide');
      slideElement.src = slideImages[step];

      slideElement.classList.add('fade-in');
      setTimeout(function () {
        slideElement.classList.remove('fade-in');
      }, 500);

      step = (step + 1) % slideImages.length;
      setTimeout(slideIt, 20000);
    }

    slideIt();
  });